#include "../../libft.h"

int	ft_strstr(char *s1, char *s2)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (s2[0] == 0)
		return (1);
	while (s1[i])
	{
		j = 0;
		while (s1[i + j] == s2[j])
		{
			j++;
			if (s2[j] == '\0')
				return (1);
		}
		i++;
	}
	return (0);
}
